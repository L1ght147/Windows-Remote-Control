def localize_text(language: Language, text: str) -> str:
    if language == "ru":
        return text
    for source, translation in sorted(EN_TEXT_REPLACEMENTS.items(), key=lambda item: len(item[0]), reverse=True):
        text = text.replace(source, translation)
    return text
